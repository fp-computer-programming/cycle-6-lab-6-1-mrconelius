# Step 1: Create an array containing the numbers 1 to 5 and store it as a variable called my_arr.
my_arr = [1, 2, 3, 4, 5]

# Step 2: Write a statement using the indexing operator that prints the number 3 in my_arr.
print("Number 3 in my_arr:", my_arr[2])  # Note: Python uses 0-based indexing, so index 2 corresponds to the third element.

# Step 3: Write a statement that prints the length of my_arr.
print("Length of my_arr:", len(my_arr))

# Step 4: Write a statement that repeats my_arr 3 times.
my_arr_repeated = my_arr * 3
print("my_arr repeated 3 times:", my_arr_repeated)

# Step 5: Write a statement that converts "string" to an array.
string_to_array = list("string")
print("String converted to an array:", string_to_array)
